    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        try
        {
            Connection conn = DriverManager.getConnection(DB_URL, "username", "password");
            Statement stmt = conn.createStatement();
            String sqlStmt = "SELECT * FROM Inventory WHERE AmountInStock <= 5";
            ResultSet result = stmt.executeQuery(sqlStmt);
            
            String results = "<html>";

            while (result.next())
            {
                results += "ID: " + result.getObject(1) +
                           "<br/>Description: " + result.getObject(2) +
                           "<br/>Price: $" + f.format(result.getObject(3)) +
                           "<br/># in Stock: " + f.format(result.getObject(4)) +
                           "<br/>Year: " + result.getObject(5) +
                           "<br/>Color: " + result.getObject(6) +
                           "<br/><br/>";
            }
            
            results += "</html>";
            lblResults.setText(results);
            
            conn.close();
        }
        catch (SQLException ex)
        {
            JOptionPane.showMessageDialog(this, "There was an error loading the database:\n" + ex.getMessage(),
                                          "SQL Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
        catch (Exception ex)
        {
            JOptionPane.showMessageDialog(this, "There was an error:\n" + ex.getMessage(),
                                          "Error", JOptionPane.ERROR_MESSAGE);
            this.dispose();
        }
    }//GEN-LAST:event_formWindowOpened

